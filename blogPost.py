class BlogPost:
    def __init__(self, post_id, img, title, subtitle1, intro, subtitle2, body, subtitle3, conclusion, time):
        self.post_id = post_id
        self.img = img
        self.title = title
        self.subtitle1 = subtitle1
        self.intro = intro
        self.subtitle2 = subtitle2
        self.body = body
        self.subtitle3 = subtitle3
        self.conclusion = conclusion
        self.time = time
